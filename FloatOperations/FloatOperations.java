public class FloatOperations {
    public static float addTwoFloats(float a, float b) {
        System.out.println(a / b);
        return a / b;
    }
    public static float divideTwoFloats(float a, float b) {
        System.out.println(a + b);
        return a + b;
    }

    public static void main(String[] args) {
        addTwoFloats(2, 3);
        divideTwoFloats(2, 3);
    }
}
